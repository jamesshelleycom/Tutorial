first_input = input("Enter first number:")
second_input = input("Enter second number:")
sum = int(first_input) + int(second_input)
substraction = int(first_input) - int(second_input)
multiplication = int(first_input) * int(second_input)
division = int(first_input) / int(second_input)
print(first_input, "+", second_input, "=", sum)
print(first_input, "-", second_input, "=", substraction)
print(first_input, "*", second_input, "=", multiplication)
print(first_input, "/", second_input, "=", division)